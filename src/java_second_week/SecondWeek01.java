package java_second_week;

public class SecondWeek01 {
    public static void main(String[] args) {
        //x + y
        int x = 5;
        int y = 10;
        int result;
        result = x + y;
        System.out.println(result);

        //x - y
        result = x - y;
        System.out.println(result);

        //x * y
        result = x * y;
        System.out.println(result);

        //x \ y
        result = x / y;
        System.out.println(result);

        result = y % x;
        System.out.println(result);
    }
}
